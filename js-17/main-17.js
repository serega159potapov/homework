let isValid = true;
let type;

const weight = +prompt("Введите вес посылки");


if (weight <= 0 || isNaN(weight)) {
    alert("Некорректный вес посылки");
    isValid = false;
}

if (isValid) {
    type = prompt("Введите тип посылки Стандарт, Экспресс, Премиум");

    if (type !== "Стандарт" && type !== "Экспресс" && type !== "Премиум") {
        alert("Неверный тип доставки");
        isValid = false;
    }
}

if (isValid) {
    const baseCost = weight < 1 ? 5 : weight > 5 ? 15 : 10;
    let ratio;
    switch (type) {
        case "Стандарт":
            ratio = 1;
            break;
        case "Экспресс":
            ratio = 1.5;
            break;
        case "Премиум":
            ratio = 2;
            break;
    }

const totalCost = baseCost * ratio;
alert(`Итоговая стоимость доставки: ${totalCost}$`);
}
